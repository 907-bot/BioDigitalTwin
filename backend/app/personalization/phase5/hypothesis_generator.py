"""
Phase 5 — Pillar 3: Scientific Hypothesis Generator.

Generates testable scientific hypotheses from:
  - Causal mechanisms discovered by Pillar 2
  - Knowledge graph associations (Pillar 1)
  - Twin population data (Phase 4 VirtualPopulation)
  - Novel associations not captured by standard biomarkers

Each hypothesis includes:
  - Title and narrative description
  - Proposed mechanism with evidence
  - Confidence estimate
  - Validation plan (what to test, how, metrics)
"""

import numpy as np
import pandas as pd
from typing import Dict, List, Optional, Tuple, Any, Callable
from dataclasses import dataclass, field
from datetime import datetime
from enum import Enum


# ── Data Types ────────────────────────────────────────────────

@dataclass
class HypothesisEvidence:
    """Evidence supporting a hypothesis."""
    source: str                # "causal_discovery", "literature", "twin_data", "knowledge_graph"
    description: str
    effect_size: float = 0.0
    confidence: float = 0.0
    p_value: float = 1.0
    n_observations: int = 0

    def __str__(self) -> str:
        return f"[{self.source}] {self.description} (β={self.effect_size:.3f}, p={self.p_value:.3f})"


@dataclass
class ValidationPlan:
    """Concrete plan to validate a hypothesis."""
    study_type: str              # "cohort_study", "randomized_trial", "in_silico", "experimental"
    population_description: str
    sample_size_estimate: int = 0
    primary_outcome: str = ""
    secondary_outcomes: List[str] = field(default_factory=list)
    duration_days: int = 0
    statistical_power: float = 0.0
    expected_effect_size: float = 0.0
    confounders_to_control: List[str] = field(default_factory=list)
    biomarkers_to_measure: List[str] = field(default_factory=list)

    def describe(self) -> str:
        return (
            f"{self.study_type} in {self.population_description} "
            f"(n={self.sample_size_estimate}, {self.duration_days}d, "
            f"power={self.statistical_power:.0%})"
        )


@dataclass
class ScientificHypothesis:
    """
    A complete, testable scientific hypothesis.

    Generated by the platform, reviewable by human scientists.
    """
    title: str
    summary: str
    proposed_mechanism: str
    evidence: List[HypothesisEvidence] = field(default_factory=list)
    overall_confidence: float = 0.0
    novelty_score: float = 0.0       # how novel vs established knowledge
    potential_impact: float = 0.0    # potential clinical/scientific impact
    validation_plan: Optional[ValidationPlan] = None
    generated_by: str = "hypothesis_generator"
    generated_at: float = 0.0
    tags: List[str] = field(default_factory=list)
    status: str = "generated"        # generated, reviewed, validated, rejected

    def __post_init__(self):
        if self.generated_at == 0.0:
            self.generated_at = __import__('time').time()

    def describe(self) -> str:
        return (
            f"**{self.title}**\n"
            f"*Confidence: {self.overall_confidence:.0%} | "
            f"Novelty: {self.novelty_score:.0%} | "
            f"Impact: {self.potential_impact:.0%}*\n"
            f"{self.summary}\n"
            f"Mechanism: {self.proposed_mechanism}\n"
            f"Evidence: {len(self.evidence)} sources\n"
            + (f"Validation: {self.validation_plan.describe()}"
               if self.validation_plan else "")
        )


